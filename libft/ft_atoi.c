int	ft_atoi(const char *nptr)
{
	int	i;
	int	at;
	int	sign;

	i = 0;
	at = 0;
	sign = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		at = at * 10 + nptr[i] - 48;
		i++;
	}
	return (at * sign);
}
